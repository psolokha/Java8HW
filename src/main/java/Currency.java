public class Currency {

    private int id;
    private String code;
    private String name_short;
    private String name_full;

    public int getId() {
        return id;
    }

    public String getCode() {
        return code;
    }

    public String getName_short() {
        return name_short;
    }

    public String getName_full() {
        return name_full;
    }
}
